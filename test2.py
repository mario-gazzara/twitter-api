def clean_twitter_account_name(account_name):
    # Remove emojis
    account_name = ''.join([char for char in account_name if not any(ord(c) > 127 for c in char)])

    # Remove "0x" at the beginning
    account_name = account_name.lstrip('0x')

    # Remove specific words and blacklisted words
    blacklist = [".bitcoin", ".eth", ".vet", ".algo", "nft"]
    words_to_remove = [word.lower() for word in blacklist]

    account_name_lower = account_name.lower()

    for word in words_to_remove:
        account_name_lower = account_name_lower.replace(word, "")

    # Remove tag after "|" or "-"
    account_name = re.split(r'[|-]', account_name_lower)[0].strip()

    if not account_name:
        return None

    # Â Capitalize first letter of each word
    account_name = account_name.title()

    return account_name


